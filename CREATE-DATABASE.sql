CREATE TABLE Player (
	id VARCHAR(100) NOT NULL PRIMARY KEY,
	first_name VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	last_name VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	team_id VARCHAR(100) NOT NULL REFERENCES Team(id),
	height INTEGER NOT NULL DEFAULT 0,
	weight INTEGER NOT NULL DEFAULT 0,
	jersey_number INTEGER NOT NULL DEFAULT 0,
	year VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	position VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	birthplace VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN'
);

CREATE TABLE GameStats (
	game_id VARCHAR(100) NOT NULL REFERENCES Game(id),
	player_id VARCHAR(100) NOT NULL REFERENCES Player(id),
	minutes INTEGER NOT NULL DEFAULT 0,
	three_point_attempts INTEGER NOT NULL DEFAULT 0,
	three_point_makes INTEGER NOT NULL DEFAULT 0,
	two_point_attempts INTEGER NOT NULL DEFAULT 0,
	two_point_makes INTEGER NOT NULL DEFAULT 0,
	field_goal_attempts INTEGER NOT NULL DEFAULT 0,
	field_goal_makes INTEGER NOT NULL DEFAULT 0,
	free_throw_attempts INTEGER NOT NULL DEFAULT 0,
	free_throw_makes INTEGER NOT NULL DEFAULT 0,
	offensive_rebounds INTEGER NOT NULL DEFAULT 0,
	defensive_rebounds INTEGER NOT NULL DEFAULT 0,
	assists INTEGER NOT NULL DEFAULT 0,
	turnovers INTEGER NOT NULL DEFAULT 0,
	steals INTEGER NOT NULL DEFAULT 0,
	blocks INTEGER NOT NULL DEFAULT 0,
	personal_fouls INTEGER NOT NULL DEFAULT 0,
	technical_fouls INTEGER NOT NULL DEFAULT 0,
	flagrant_fouls INTEGER NOT NULL DEFAULT 0,
	points INTEGER NOT NULL DEFAULT 0,
	PRIMARY KEY (game_id, player_id)
);

CREATE TABLE Score (
	game_id VARCHAR(100) NOT NULL REFERENCES Game(id),
	home_score INTEGER NOT NULL DEFAULT 0,
	away_score INTEGER NOT NULL DEFAULT 0,
	PRIMARY KEY(game_id)
);
