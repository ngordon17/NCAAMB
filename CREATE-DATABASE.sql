CREATE TABLE Venue (
	id VARCHAR(100) NOT NULL PRIMARY KEY,
	name VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	address VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	city VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	state CHAR(2) NOT NULL DEFAULT 'UNKNOWN',
	country VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	zipcode INTEGER NOT NULL DEFAULT 00000,
	capacity INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE Conference (
	id VARCHAR(100) NOT NULL PRIMARY KEY,
	name VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	alias VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN'
);

CREATE TABLE Team (
	id VARCHAR(100) NOT NULL PRIMARY KEY,
	name VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	alias VARCHAR(100) NOT NULL DEFAULT 'UNKNOWN',
	venue_id VARCHAR(100) REFERENCES Venue(id),
	conference_id VARCHAR(100) NOT NULL REFERENCES Conference(id)
);

